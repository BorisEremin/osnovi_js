function findMax(a, b, c) {
    let max = a;
}

if (b > max) {
    max = b;
}

if (c > max) {
    max = c;
}

let num1 = parseFloat('Введите первое число:');
let num2 = parseFloat('Введите второе число:');
let num3 = parseFloat('Введите третье число:');

let max = findMax(num1, num2, num3);
console.log('Максимальное значение:', max);


